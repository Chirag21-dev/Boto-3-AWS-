# Dictionary
dict_var = {'a': 1, 'b': 2, 'c': 3}
print("Dictionary Variable:", dict_var)

# Set
set_var = {1, 2, 3, 4, 5}
print("Set Variable:", set_var)

# Dictionary operations
dict_var['d'] = 4
del dict_var['a']
print("Modified Dictionary:", dict_var)

# Set operations
set_var.add(6)
set_var.remove(3)
print("Modified Set:", set_var)
